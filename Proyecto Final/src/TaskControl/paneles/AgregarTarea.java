    private void crearTarea() {
        String nombreTareaCampo = txtNombreTarea.getText();
        String descripcionTareaCampo = taDescripcionTarea.getText();

        if (nombreTareaCampo.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debes colocar el nombre de la tarea.");
            txtNombreTarea.requestFocus();
            return;
        }

        Tableros tarea = new Tableros();
        tarea.setLista_tareas(nombreTareaCampo);
        tarea.setDescripcion_tarea(descripcionTareaCampo);

        try {
            DAOTablero dao = new DAOTableroImpl();
            dao.agregarTarea(tarea);

            JOptionPane.showMessageDialog(null, "Tarea agregada satisfactoriamente.");

            txtNombreTarea.setText("");
            taDescripcionTarea.setText("");

            mostrarPanel(new Tablero());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: No se pudo agregar la tarea.");
        }
    }

